def _nearest_challenge_point(lat: float, lon: float) -> dict | None:
    """Return the nearest DATA_CHALLENGE_50 point to (lat, lon)."""
    df = _load_challenge_df()
    dlat = df["latitude"].values  - lat
    dlon = df["longitude"].values - lon
    dist = dlat**2 + dlon**2
    idx  = int(np.argmin(dist))
    row  = df.iloc[idx]
    return {
        "location_id": str(row["location_id"]),
        "lat":         float(row["latitude"]),
        "lon":         float(row["longitude"]),
        "dist_m":      float(np.sqrt(dist[idx]) * 111_320),
    }


def _png_b64(result: dict, location_id: str) -> str:
    """Generate a 5-panel PNG for result and return as base64 string."""
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt

    result["label"] = location_id
    buf = io.BytesIO()
    try:
        fig_path = OUT_DIR / "reports" / location_id / f"{location_id}.png"
        fig_path.parent.mkdir(parents=True, exist_ok=True)
        _main_mod._plot_point(result, fig_path)
        png_bytes = fig_path.read_bytes()
    except Exception as exc:
        logger.warning(f"Plot failed: {exc}")
        return ""
    return base64.b64encode(png_bytes).decode()


def _save_analysis_result(result: dict, location_id: str):
    """Persist JSON + PNG for a completed analysis result (sync, safe to run in executor)."""
    json_path = OUT_DIR / "reports" / location_id / f"{location_id}.json"
    json_path.parent.mkdir(parents=True, exist_ok=True)
    _main_mod._save_json(result, json_path)
    _png_b64(result, location_id)


async def _run_analysis_job(job_id: str, lat: float, lon: float,
                             location_id: str, run_search: bool):
    """Background coroutine: run analyze_location in the thread pool, store result."""
    loop = asyncio.get_event_loop()
    t0   = time.time()
    try:
        result = await loop.run_in_executor(
            _analysis_pool,
            lambda: analyze_location(lat, lon, run_local_search=run_search),
        )
        await loop.run_in_executor(
            _analysis_pool,
            lambda: _save_analysis_result(result, location_id),
        )
        api_result = _result_to_api(result, location_id)
        api_result["total_elapsed_s"] = round(time.time() - t0, 1)
        _jobs[job_id] = {"status": "done", "result": api_result}
        global _precomputed, _sample_cache
        _precomputed = None
        _sample_cache = None
        logger.info(f"[job:{job_id}] done in {time.time()-t0:.1f}s")
    except Exception as exc:
        logger.error(f"[job:{job_id}] failed: {exc}")
        _jobs[job_id] = {"status": "error", "error": str(exc)}


async def _run_bbox_job(job_id: str, subset: pd.DataFrame):
    """Background coroutine: run bbox batch analysis point-by-point in thread pool."""
    loop     = asyncio.get_event_loop()
    n        = len(subset)
    results_out: list[dict] = []
    errors:      list[dict] = []

    for i, (_, row) in enumerate(subset.iterrows(), 1):
        lid = str(row["location_id"])
        _jobs[job_id]["completed"] = i - 1
        try:
            res = await loop.run_in_executor(
                _analysis_pool,
                lambda r=row: analyze_location(
                    float(r["latitude"]), float(r["longitude"]), run_local_search=False
                ),
            )
            await loop.run_in_executor(
                _analysis_pool,
                lambda r=res, l=lid: _save_analysis_result(r, l),
            )
            results_out.append(_result_to_api(res, lid, source="aoi_batch"))
        except Exception as exc:
            errors.append({"location_id": lid, "error": str(exc)})

    _jobs[job_id].update({
        "status":   "done",
        "completed": n,
        "results":  results_out,
        "errors":   errors,
        "n_points": n,
    })
    global _precomputed, _sample_cache
    _precomputed = None
    _sample_cache = None
    logger.info(f"[bbox_job:{job_id}] done — {len(results_out)}/{n} ok, {len(errors)} errors")


def _result_to_api(result: dict, location_id: str, source: str = "on-demand") -> dict:
    """Strip numpy arrays and add API-friendly fields."""
    rk  = result.get("risk", {})
    cl  = result.get("classification", {})
    bn  = result.get("best_nearby") or {}
    imp = bn.get("improvement") or {}
    return {
        "location_id":    location_id,
        "lat":            result["lat"],
        "lon":            result["lon"],
        "feasible":       result["feasible"],
        "failure_mode":   result.get("failure_mode", ""),
        "risk_score":     rk.get("risk_score", 0),
        "risk_tier":      rk.get("risk_tier", "low"),
        "dominant":       cl.get("dominant", "clear"),
        "slope_deg":      result.get("slope_deg", 0),
        "canopy_max_m":   result.get("canopy_max_m", 0),
        "building_count": result.get("building_count", 0),
        "warnings":       "; ".join(result.get("warnings", [])),
        "elapsed_s":      result.get("elapsed_s", 0),
        "best_nearby": {
            "lat":        bn.get("best", {}).get("lat") if bn.get("best") else None,
            "lon":        bn.get("best", {}).get("lon") if bn.get("best") else None,
            "risk_score": bn.get("best", {}).get("risk_score") if bn.get("best") else None,
            "distance_m": imp.get("distance_m"),
            "risk_delta": imp.get("risk_delta"),
            "feasible_gained": imp.get("feasible_gained"),
        } if bn else None,
        "_source":        source,
        "png_url":        f"/outputs/reports/{location_id}/{location_id}.png",
    }


