@app.get("/api/all_points")
async def all_points():
    """Return a stratified 20K sample of ALL dataset points for map display.

    Each point includes: location_id, lat, lon, and whether it has been analyzed.
    Pre-computed points include their risk_tier so the frontend can color them.
    Unanalyzed points are shown as gray dots that can be clicked to compute.
    """
    global _sample_cache
    if _sample_cache is None:
        df = _load_challenge_df()
        precomputed = _load_precomputed()

        # Build set of analyzed location_ids
        analyzed_ids = set()
        analyzed_map = {}  # location_id → {risk_tier, risk_score, feasible, ...}
        for r in precomputed:
            lid = str(r.get("location_id", ""))
            analyzed_ids.add(lid)
            analyzed_map[lid] = r

        # Stratified sample: take ~20K points spread across all counties
        sample = df.sample(min(MAP_SAMPLE_SIZE, len(df)), random_state=42)

        # Ensure ALL analyzed points are included (even if not in random sample)
        analyzed_lids_in_sample = set(sample["location_id"].astype(str)) & analyzed_ids
        missing_analyzed = analyzed_ids - analyzed_lids_in_sample
        if missing_analyzed:
            extras = df[df["location_id"].astype(str).isin(missing_analyzed)]
            sample = pd.concat([sample, extras], ignore_index=True).drop_duplicates(
                subset="location_id", keep="first"
            )

        rows = []
        for _, r in sample.iterrows():
            lid = str(r["location_id"])
            if lid in analyzed_map:
                am = analyzed_map[lid]
                lat = am.get("lat") or am.get("latitude")
                lon = am.get("lon") or am.get("longitude")
                rows.append({
                    "i": lid,
                    "a": round(float(lat), 5),
                    "o": round(float(lon), 5),
                    "z": 1,  # analyzed
                    "t": str(am.get("risk_tier", "low")),
                    "f": bool(am.get("feasible", True)),
                    "s": round(float(am.get("risk_score", 0)), 1),
                })
            else:
                rows.append({
                    "i": lid,
                    "a": round(float(r["latitude"]), 5),
                    "o": round(float(r["longitude"]), 5),
                    "z": 0,  # not analyzed
                })

        _sample_cache = rows
        logger.info(f"[api/all_points] Built sample: {len(rows)} pts "
                    f"({sum(1 for r in rows if r['z'])} analyzed)")

    return JSONResponse(content=_sample_cache)


@app.post("/api/analyze")
async def analyze(req: AnalyzeRequest):
    """
    On-demand single-point analysis — returns a job_id immediately (HTTP 202).

    The client should poll GET /api/status/{job_id} until status == "done" or "error".
    Analysis runs in a background thread so the event loop stays unblocked.
    """
    logger.info(f"[api/analyze] lat={req.lat:.5f} lon={req.lon:.5f} id={req.location_id}")
    job_id = uuid.uuid4().hex[:12]
    _jobs[job_id] = {"status": "pending", "location_id": req.location_id,
                     "lat": req.lat, "lon": req.lon}
    asyncio.create_task(
        _run_analysis_job(job_id, req.lat, req.lon, req.location_id, req.run_search)
    )
    return JSONResponse(
        status_code=202,
        content={"job_id": job_id, "status": "pending", "location_id": req.location_id},
    )


@app.get("/api/status/{job_id}")
async def job_status(job_id: str):
    """
    Poll the status of a background analysis job.

    Returns:
        pending  — job is queued or running
        done     — job complete; "result" key holds the analysis output
        error    — job failed; "error" key holds the exception message
    """
    job = _jobs.get(job_id)
    if job is None:
        raise HTTPException(status_code=404, detail=f"Job {job_id} not found")
    return JSONResponse(content=job)


@app.get("/api/nearest")
async def nearest(lat: float, lon: float):
    """Return the nearest DATA_CHALLENGE_50 point to (lat, lon)."""
    pt = _nearest_challenge_point(lat, lon)
    if pt is None:
        raise HTTPException(status_code=404, detail="Dataset not available")
    return JSONResponse(content=pt)


@app.post("/api/analyze_bbox")
async def analyze_bbox(req: BboxRequest):
    """
    Batch analysis for an AOI bounding box drawn on the map.

    Flow:
      1. If bbox > threshold and confirmed=False → return confirmation_required (200, not 202)
         so the frontend can show a dialog, then re-submit with confirmed=True.
      2. Otherwise → start async job, return job_id immediately (HTTP 202 with status="running").
      3. Client polls GET /api/status/{job_id}; the job dict includes "completed" / "total"
         for live progress display.
    """
    df = _load_challenge_df()
    mask = (
        (df["latitude"]  >= req.lat_min) & (df["latitude"]  <= req.lat_max) &
        (df["longitude"] >= req.lon_min) & (df["longitude"] <= req.lon_max)
    )
    subset = df[mask].copy()
    n = len(subset)

    if n == 0:
        return JSONResponse(content={"status": "empty", "n_points": 0, "results": []})

    if n > AOI_CONFIRMATION_THRESHOLD and not req.confirmed:
        # Use HTTP 200 so the frontend can inspect status field without re-parsing 202
        return JSONResponse(
            status_code=200,
            content={
                "status":    "confirmation_required",
                "n_points":  n,
                "threshold": AOI_CONFIRMATION_THRESHOLD,
                "message":   (
                    f"This AOI contains {n:,} points (~{n * 30 // 60} min). "
                    f"Re-submit with confirmed=true to proceed."
                ),
            },
        )

    # Cap to threshold
    if n > AOI_CONFIRMATION_THRESHOLD:
        subset = subset.sample(AOI_CONFIRMATION_THRESHOLD, random_state=42)
        n = AOI_CONFIRMATION_THRESHOLD
        logger.info(f"[api/analyze_bbox] Sampled {AOI_CONFIRMATION_THRESHOLD} pts")

    job_id = uuid.uuid4().hex[:12]
    _jobs[job_id] = {"status": "running", "total": n, "completed": 0}
    logger.info(f"[api/analyze_bbox] job={job_id} n={n}")
    asyncio.create_task(_run_bbox_job(job_id, subset))

    return JSONResponse(
        status_code=202,
        content={"job_id": job_id, "status": "running", "n_points": n},
    )


@app.get("/outputs/{path:path}")
async def serve_output(path: str):
    """Serve files from the outputs/ directory."""
    fp = OUT_DIR / path
    if not fp.exists():
        raise HTTPException(status_code=404, detail="File not found")
    return FileResponse(str(fp))
