class AnalyzeRequest(BaseModel):
    lat: float
    lon: float
    location_id: str = "on-demand"
    run_search:  bool = True


class BboxRequest(BaseModel):
    lat_min: float
    lat_max: float
    lon_min: float
    lon_max: float
    confirmed: bool = False   # True = user already clicked "Run anyway" for >100 pts


