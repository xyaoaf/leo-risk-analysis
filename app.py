"""
app.py — FastAPI web application for LEO satellite risk analysis.

Provides a local interactive map with:
  • Esri World Imagery satellite tile background (via Leaflet.js)
  • Pre-computed risk results as clickable markers (popup with 5-panel PNG)
  • Click-anywhere → nearest DATA_CHALLENGE_50 point lookup → on-demand analysis
  • Leaflet.Draw rectangle → AOI batch analysis (100-point confirmation gate)

Usage:
    conda run -n cs378 uvicorn app:app --reload --port 8000
    # Then open http://localhost:8000 in a browser

Design:
    - All analysis runs synchronously in-process (simple, no task queue)
    - Pre-computed results served from challenge50 + nc_test JSON files
    - Nearest-point lookup uses vectorised numpy distance (no spatial index needed
      for <5M points once loaded; first request cold-loads the CSV in ~5s)
    - PNGs are generated by _plot_point() from main.py (same as CLI)
    - Structured for future deployment: replace localhost uvicorn with gunicorn,
      add auth middleware, swap local PNG serve with S3 presigned URLs

Endpoints:
    GET  /                        — interactive map HTML
    GET  /api/precomputed         — all pre-computed results (JSON)
    POST /api/analyze             — single-point on-demand analysis
    POST /api/analyze_bbox        — batch analysis for a drawn bounding box
    GET  /outputs/{path:path}     — serve PNG / JSON files from outputs/
"""

